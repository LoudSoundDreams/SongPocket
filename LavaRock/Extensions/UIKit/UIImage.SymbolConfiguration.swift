import UIKit

extension UIImage.SymbolConfiguration {
	static func initWithPointSize48() -> Self {
		return Self.init(pointSize: 48)
	}
	
	static func initWithPointSize96() -> Self {
		return Self.init(pointSize: 96)
	}
}
